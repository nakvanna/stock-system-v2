import { Injectable } from '@nestjs/common';
import { InjectModel } from '@nestjs/mongoose';
import { Model } from 'mongoose';
import { ErrorHandlingMessage } from '../../shared/utils';
import {
  applyPagination,
  InputCursorPaginationOption,
} from '../../shared/cursor-pagination';
import { CustomerModel } from './models/customer.model';
import { CreateCustomerInput } from './input/create-customer.input';
import { CustomerCursorPagination } from './dto/customer.dto';
import {UpdateCustomerInput} from "./input/update-customer.input";

@Injectable()
export class CustomerService {
  constructor(@InjectModel('Customer') private model: Model<CustomerModel>) {}

  async create(
    create_input: CreateCustomerInput,
  ): Promise<CustomerModel | ErrorHandlingMessage> {
    try {
      const model = new this.model(create_input);
      const data = await model.save();
      data.message = 'បានរក្សាទុក';
      data.success = true;
      return data;
    } catch (e) {
      return {
        message: e.message.split(':')[0],
        success: false,
      };
    }
  }
  async findAll(
    options: InputCursorPaginationOption,
  ): Promise<CustomerCursorPagination> {
    return await applyPagination(this.model, options);
  }

  async findOne(id: string): Promise<CustomerModel | ErrorHandlingMessage> {
    try {
      const data = await this.model.findById(id);
      data.message = 'បានស្វែងរក';
      data.success = true;
      return data;
    } catch (e) {
      return {
        message: e.message.split(':')[0],
        success: false,
      };
    }
  }

  async update(
    id: string,
    update_input: UpdateCustomerInput,
  ): Promise<CustomerModel | ErrorHandlingMessage> {
    try {
      const data = await this.model.findByIdAndUpdate(
        id,
        { $set: update_input },
        { new: true },
      );
      data.message = 'បានកែប្រែ';
      data.success = true;
      return data;
    } catch (e) {
      return {
        message: e.message.split(':')[0],
        success: false,
      };
    }
  }

  async remove(id: string): Promise<CustomerModel | ErrorHandlingMessage> {
    try {
      const data = await this.model.findById(id);
      await this.model.findByIdAndDelete(id);
      data.message = 'បានលុប';
      data.success = true;
      return data;
    } catch (e) {
      return {
        message: e.message.split(':')[0],
        success: false,
      };
    }
  }

  async findBySale(_id: string): Promise<CustomerModel> {
    return this.model.findOne({_id})
  }
}
